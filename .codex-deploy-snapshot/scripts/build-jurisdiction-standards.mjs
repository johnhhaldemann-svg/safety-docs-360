import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const manifestPath = path.join(repoRoot, "lib", "jurisdictionStandards", "source-manifest.json");
const outputDir = path.join(repoRoot, "lib", "jurisdictionStandards", "generated");

function dedupeBy(items, keyFn) {
  const map = new Map();
  for (const item of items) {
    const key = keyFn(item);
    if (!key) continue;
    map.set(key, item);
  }
  return [...map.values()];
}

function normalizeManifest(input) {
  const jurisdictions = dedupeBy(input?.jurisdictions ?? [], (item) => item?.code).map((item) => ({
    ...item,
    metadata: item?.metadata && typeof item.metadata === "object" ? item.metadata : {},
  }));

  const standards = dedupeBy(input?.standards ?? [], (item) => item?.id).map((item) => ({
    ...item,
    applicability:
      item?.applicability && typeof item.applicability === "object" ? item.applicability : {},
    content: item?.content && typeof item.content === "object" ? item.content : {},
    metadata: item?.metadata && typeof item.metadata === "object" ? item.metadata : {},
  }));

  const mappings = dedupeBy(input?.mappings ?? [], (item) => item?.id).map((item) => ({
    ...item,
    metadata: item?.metadata && typeof item.metadata === "object" ? item.metadata : {},
  }));

  return { jurisdictions, standards, mappings };
}

function sqlString(value) {
  if (value === null || value === undefined) {
    return "null";
  }
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlJson(value) {
  return `${sqlString(JSON.stringify(value ?? {}))}::jsonb`;
}

function sqlDate(value) {
  return value ? `${sqlString(value)}::date` : "null";
}

function buildSql(config) {
  const jurisdictionRows = config.jurisdictions
    .map(
      (item) =>
        `(${sqlString(item.code)}, ${sqlString(item.stateCode)}, ${sqlString(item.displayName)}, ${sqlString(item.planType)}, ${item.coversPrivateSector !== false ? "true" : "false"}, ${sqlString(item.sourceUrl)}, ${sqlString(item.sourceTitle)}, ${sqlString(item.sourceAuthority)}, ${sqlDate(item.effectiveDate)}, ${sqlDate(item.lastReviewedDate)}, ${sqlJson(item.metadata)})`
    )
    .join(",\n");

  const standardRows = config.standards
    .map(
      (item) =>
        `(${sqlString(item.id)}, ${sqlString(item.jurisdictionCode)}, ${sqlString(item.surfaceScope)}, ${sqlString(item.standardType)}, ${sqlString(item.title)}, ${sqlString(item.summary)}, ${sqlJson(item.applicability)}, ${sqlJson(item.content)}, ${sqlString(item.sourceUrl)}, ${sqlString(item.sourceTitle)}, ${sqlString(item.sourceAuthority)}, ${sqlDate(item.effectiveDate)}, ${sqlDate(item.lastReviewedDate)}, ${sqlJson(item.metadata)})`
    )
    .join(",\n");

  const mappingRows = config.mappings
    .map(
      (item) =>
        `(${sqlString(item.id)}, ${sqlString(item.standardId)}, ${sqlString(item.mappingType)}, ${sqlString(item.mappingKey)}, ${sqlJson(item.metadata)})`
    )
    .join(",\n");

  return `-- Generated by scripts/build-jurisdiction-standards.mjs
insert into public.platform_jurisdictions (
  code,
  state_code,
  display_name,
  plan_type,
  covers_private_sector,
  source_url,
  source_title,
  source_authority,
  effective_date,
  last_reviewed_date,
  metadata
)
values
${jurisdictionRows}
on conflict (code) do update
set
  state_code = excluded.state_code,
  display_name = excluded.display_name,
  plan_type = excluded.plan_type,
  covers_private_sector = excluded.covers_private_sector,
  source_url = excluded.source_url,
  source_title = excluded.source_title,
  source_authority = excluded.source_authority,
  effective_date = excluded.effective_date,
  last_reviewed_date = excluded.last_reviewed_date,
  metadata = excluded.metadata,
  updated_at = now();

insert into public.platform_jurisdiction_standards (
  id,
  jurisdiction_code,
  surface_scope,
  standard_type,
  title,
  summary,
  applicability,
  content,
  source_url,
  source_title,
  source_authority,
  effective_date,
  last_reviewed_date,
  metadata
)
values
${standardRows}
on conflict (id) do update
set
  jurisdiction_code = excluded.jurisdiction_code,
  surface_scope = excluded.surface_scope,
  standard_type = excluded.standard_type,
  title = excluded.title,
  summary = excluded.summary,
  applicability = excluded.applicability,
  content = excluded.content,
  source_url = excluded.source_url,
  source_title = excluded.source_title,
  source_authority = excluded.source_authority,
  effective_date = excluded.effective_date,
  last_reviewed_date = excluded.last_reviewed_date,
  metadata = excluded.metadata,
  updated_at = now();

insert into public.platform_jurisdiction_standard_mappings (
  id,
  standard_id,
  mapping_type,
  mapping_key,
  metadata
)
values
${mappingRows}
on conflict (id) do update
set
  standard_id = excluded.standard_id,
  mapping_type = excluded.mapping_type,
  mapping_key = excluded.mapping_key,
  metadata = excluded.metadata,
  updated_at = now();
`;
}

async function main() {
  const raw = JSON.parse(await readFile(manifestPath, "utf8"));
  const normalized = normalizeManifest(raw);

  await mkdir(outputDir, { recursive: true });
  await writeFile(
    path.join(outputDir, "normalized-config.json"),
    `${JSON.stringify(normalized, null, 2)}\n`,
    "utf8"
  );
  await writeFile(path.join(outputDir, "seed.sql"), buildSql(normalized), "utf8");

  console.log(
    `Wrote ${normalized.jurisdictions.length} jurisdictions, ${normalized.standards.length} standards, and ${normalized.mappings.length} mappings to ${outputDir}`
  );
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
